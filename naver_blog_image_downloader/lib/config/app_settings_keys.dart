/// SharedPreferences 中使用者偏好設定的 key 常數。
abstract final class AppSettingsKeys {
  /// 主題模式的儲存 key。
  static const String themeMode = 'app_theme_mode';

  /// 語系的儲存 key。
  static const String locale = 'app_locale';

  /// 上次顯示「版本新功能」或「首次安裝引導」頁面時的 App 版本號。
  static const String lastSeenVersion = 'app_last_seen_version';
}
